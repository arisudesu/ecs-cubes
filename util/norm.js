function calculateNorm(face) {
    const u = [
        face[1][0] - face[0][0],
        face[1][1] - face[0][1],
        face[1][2] - face[0][2]
    ];
    const v = [
        face[2][0] - face[0][0],
        face[2][1] - face[0][1],
        face[2][2] - face[0][2]
    ];
    return [
        u[1] * v[2] - u[2] * v[1],
        u[2] * v[0] - u[0] * v[2],
        u[0] * v[1] - u[1] * v[0]
    ];
}

